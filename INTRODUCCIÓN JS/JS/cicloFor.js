const nombres = ["Carlos", "Cristian", "Estefania", "Andres", "Erika", "Jesus"];

nombres.forEach((nombre)=>{
    console.log(nombre)
});
/*
    📌 Ciclo For
    Repite un bloque de código mientras se cumpla una condición.
    
    Expresión 1: Se ejecuta una sola vez antes de comcenzar a repetir el bloque de código.
    Expresión 2: Una condición, mientras se cumpla se ejecutará el bloque de código.
    Expresión 3: Esta expresión se ejecuta siempre y después de que se ejecute el bloque de código.

*/

for (let numero = 1; numero <= 100; numero++) {
    console.log(numero);
};


